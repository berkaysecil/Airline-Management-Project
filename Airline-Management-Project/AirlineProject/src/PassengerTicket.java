    private void GetFlights(){
        
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3308/AirlineDb","root","");
            St = Con.createStatement();
            String Query = "select * from FlightTbl";
            Rs = St.executeQuery(Query);
            while(Rs.next()){
                String FCode = Rs.getString("FlCode");
                PCodeCb.addItem(FCode);
            }
        } catch (SQLException ex) {
            
        }
    }
    
    
    private void GetPassenger(){
        
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3308/AirlineDb","root","");
            St = Con.createStatement();
            String Query = "select * from PassengerRegisterTbl";
            Rs = St.executeQuery(Query);
            while(Rs.next()){
                String PId = String.valueOf(Rs.getInt("pass_id"));
                PassIdCb.addItem(PId);
            }
        } catch (SQLException ex) {
            
        }
    }
    
    
    private void GetPassengerData(){
        String Query = "select * from PassengerRegisterTbl where pass_id="+PassIdCb.getSelectedItem().toString();
        Statement St;
        ResultSet Rs;
        
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3308/AirlineDb","root","");
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if(Rs.next()){
                PNameTb.setText(Rs.getString("pass_name"));
                GenTb.setText(Rs.getString("pass_gender"));
                PNumTb.setText(Rs.getString("pass_passport"));
                NationalityTb.setText(Rs.getString("pass_nationality"));
            }
        } catch (Exception e) {
        }
    }
    
    
    
    
     
